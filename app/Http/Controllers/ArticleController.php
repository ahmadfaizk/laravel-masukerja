    {
        if ($request->ajax()) {
            $data = Article::all();
            return DataTables::of($data)
                ->addIndexColumn()
                ->editColumn('id_kategory', function(Article $article) {
                    return $article->category->name;
                })
                ->make(true);
        }
        return view('article');
    }

